import os
import math

def main():
    a = 1.0
    b = 2.0
    c = 3.0
    str_a = ""
    str_b = ""
    str_c = ""
    print(str_a, "%f", a)
    print(str_b, "%f", b)
    print(str_c, "%f", c)

if __name__ == "__main__":
    main()


